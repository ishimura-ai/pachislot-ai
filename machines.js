function initMySlotForAllMachines() {
    for (const key in MACHINE_DATABASE) {
        const m = MACHINE_DATABASE[key];
        if (m.type === 'AT' && !m.mySlotConfig) {
            m.mySlotConfig = {
                ...GENERIC_MYSLOT_CONFIG,
                machineName: m.name,
                enabled: true
            };
        }
    }
}
